public class test09 {
    public static void main(String[] args) {
        int n=100;
        print(n);
        for (int i=0;i<i;i++){
            print(i);
        }
        for (int i=0;i<n;i++){
            for (int j = 0; j < n; j++) {
                print(i);
            }
        }
    }

    /**
     * 时间复杂度O(n)
     * @param i
     */
    private static void print(int i){
        for (int j=0;j<i;j++){
            print(i);
        }
    }
}
